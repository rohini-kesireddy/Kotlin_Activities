package com.example.samplep1

import java.util.*

fun main(){
    var sc = Scanner(System.`in`)
    var a = sc.nextInt()
    var b = sc.nextInt()

    var sum = (a+b)
    var sub = (a-b)
    var mul = (a*b)
    var div = (a/b)
    println("Addition of two numbers is $sum")
    println("Subtraction of two numbers is $sub")
    println("Multiplication of two numbers is $mul")
    println("Division of two numbers is $div")

}